public class Average {
    public static void main(String[] args) {

        int marks1 = 65;
        int marks2 = 50;
        int marks3 = 30;
        int total = marks1+marks2+marks3;
        int avg = total/3;

        System.out.println("Marks1\t\t"+marks1);
        System.out.println("Marks2\t\t"+marks2);
        System.out.println("Marks3\t\t"+marks3);
        System.out.println("Total\t\t"+total);
        System.out.println("Average\t\t"+avg);
    }
}
